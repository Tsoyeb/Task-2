# Use Replace function to substitute ! with a space. 
sentence = 'The!quick!brown!fox!jumps!over!the!lazy!dog!'
sentence_2 = sentence.replace('!',' ')
print(sentence_2)

# Reprint sentence as Capitals 
sentence_3 = sentence_2.upper()
print(sentence_3)

# Print the sentence in reverse
print(sentence_3[::-1])
